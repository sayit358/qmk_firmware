bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    set_keylog(keycode, record);
  }
  switch(keycode) {
      /* KEYBOARD PET STATUS START */

case MT(MOD_LCTL,KC_ESC):
  if (record->event.pressed) {
      isSneaking = true;
  } else {
      isSneaking = false;
  }
  break;
case KC_SPC:
  if (record->event.pressed) {
      isJumping  = true;
      showedJump = false;
  } else {
      isJumping = false;
  }
 
  break;

  /* KEYBOARD PET STATUS END */
    }
  return true;
}

static void print_status_narrow(void) {
    /* oled_set_cursor(0, 0); */
    /* render_logo(); */
    /* wpm counter */
    uint8_t n = get_current_wpm();
    char    wpm_str[4];
    oled_set_cursor(0, 8);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write(wpm_str, false);

    oled_set_cursor(0, 9);
    oled_write(" wpm", false);
    /* Print current mode */
    oled_set_cursor(0, 0);

    /* Print current layer */
    oled_write("LAYER", false);

    oled_set_cursor(0, 1);

    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write("Base ", false);
            autoshift_enable();
            break;
        case 2:
            oled_write("Lower", false);
            autoshift_enable();
            break;
        case 1:
            oled_write("Games", false);
            autoshift_disable();
            break;
        case 3:
            oled_write("Raise", false);
            autoshift_enable();
            break;
        default:
            oled_write("Undef", false);
            autoshift_enable();
    }

    /* caps lock */
    oled_set_cursor(0, 4);
    oled_write("CPSLK", led_usb_state.caps_lock);

    /* KEYBOARD PET RENDER START */

    render_luna(0, 13);

    /* KEYBOARD PET RENDER END */
}
